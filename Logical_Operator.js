
// Logical AND
let age=20;
if(age>=18 && age<=21)
{
    console.log("you are eligible" );
}
else
{
console.log("you are not eligible");
    
}
// you are eligible



// Logical OR
let Aadhar_Age=20;
if(Aadhar_Age>=18 || Aadhar_Age>=21)
{
    console.log("you are eligible");
}
else
{
console.log("you are not eligible");
    
}
// you are eligible




// Logical NOT
let A=20;
if (!A>=20)
{
    console.log("you are eligible")
    
}
else
{
console.log("you are not eligible");
    
}
// you are not eligible













